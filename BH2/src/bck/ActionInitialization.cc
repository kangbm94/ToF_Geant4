//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

ActionInitialization::ActionInitialization(AnalysisManager* AnalysisMan,BeamManager* BeamMan)
 : G4VUserActionInitialization()
{
	ana=AnalysisMan;
	beam=BeamMan;
//	new PrimaryGeneratorAction(beam);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

ActionInitialization::~ActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void ActionInitialization::BuildForMaster() const
{
  SetUserAction(new RunAction(ana));
//  SetUserAction(new PrimaryGeneratorAction(beam));
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void ActionInitialization::Build() const
{
  SetUserAction(new RunAction(ana));
  SetUserAction(new EventAction(ana));
  SetUserAction(new PrimaryGeneratorAction(beam));
  SetUserAction(new SteppingAction(ana));
  SetUserAction(new StackingAction());
}

